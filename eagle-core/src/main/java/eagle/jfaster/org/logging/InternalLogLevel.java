package eagle.jfaster.org.logging;

/**
 * The log level that {@link eagle.jfaster.org.logging.InternalLogger} can log at.
 */
public enum InternalLogLevel {
  /**
   * 'TRACE' log level.
   */
  TRACE,
  /**
   * 'DEBUG' log level.
   */
  DEBUG,
  /**
   * 'INFO' log level.
   */
  INFO,
  /**
   * 'WARN' log level.
   */
  WARN,
  /**
   * 'ERROR' log level.
   */
  ERROR
}
